<h2><b>Advanced</b></h2>
<p>
<img border="0" src="/images/static_page/p2p/dc_v0.261.gif" width="439" height="461"></p>

<p><b>&quot;Rollback&quot;</b> - опция позволяет избежать порчи файлов при 
смене пользователей, убирая указанное количество данных каждый раз 
Вы возобновляете разгрузку.<br>
<b>&quot;Write Size Buffer&quot; </b>- я точно не знаю.Что то связано с фрагментацией.<br>
<b>&quot;Clear search box after each search&quot;</b> - очищает строку поиска 
после каждого поиска.<br>
<b>&quot;Automatically search for alternative download location&quot;</b> 
- автоматически ищет другого человека,чтобы скачать ваш файл,если 
то человек у которого вы качали off-line.<br>

<b>&quot;Open the download queue window at starpup&quot;</b> - открывает окошко 
с файлами, которые вы качаете или которые ожидают своей очереди 
на закачку.<br>
<b>&quot;Automatically follow redirects&quot;</b> - автоматически следует 
переадресации (если хаб полный и т.д.)<br>
<b>&quot;Enable SFV checking&quot;</b> - Много загрузок в DC содержат sfv 
файл, чтобы проверить целостность загрузки. DC может непрерывно 
вычислять значения CRC 32 для<br>
файла и сравнивает с sfv файлом. Если проверка терпит неудачу,<br>

файл автоматически загружен еще раз от того же самого пользователя<br>
и если это терпит неудачу, пользователь удален как источник. Для 
этого, чтобы работать,sfv файл должен быть в том же самом целевом 
каталоге, поскольку загрузка идет.</p>
<p>При работе с DC++ вы видите странные значки в скобках возле ников, 
расшифровываю</p>
<p>
<img border="0" src="/images/static_page/p2p/dc_stealth.gif" width="415" height="99"></p>
<p>&lt;++ V:x,M:x,H:x/y/z,S:x[,O:x]&gt;<br>
<br>

<b>V</b> - версия используемой программы <br>
<b>M </b>- статус пользователя, А - актив, Р - пассив (за firewall)
<br>
<b>H </b>- количество хабов на которых находится пользователь
<br>
<b>&nbsp; x</b> - количество хабов на которых вы не зарегистрированый 
пользователь <br>

<b>&nbsp; y</b> - количество хабов на которых вы зарегистрированый 
пользователь<br>
<b>&nbsp; z </b>- количество хабов на которых вы оператор хаба
<br>
<b>S</b> - количество слотов для скачивания.<br>
<b>O</b> - если общий upload - ниже этого значения DC , откроет 
другой слот <br>

Если возле ника вы видите ключ, это значит человек - админ или оператор 
на хабе. Если возле ника кирпичики, значит человек за firewall</p>
<p><b>И еще несколько интересных возможностей</b> :<br>
<br>
<b>1.</b> Вы можете посмотреть что ищут другие люди. Для этого вам 
надо зайти в - <b>File , search spy</b>.Увидев там что-то хорошее,смело 
жмите правую кнопку мыши и <b>search</b>.<br>

<b>2.</b> Что бы каждый раз при старте DC++ не писать адреса для 
коннекта, делайте следующее : <b>File , Favorite hubs</b> (Ctrl+F). 
Тут указывайте название хаба и его IP , а рядом с ним ставьте галочку.<br>
<b>3.</b> Вы можете смело выключать DC++, даже если файл полностью 
не скачен. При новом запуске програмка продолжит скачивание с того 
же самого места, при условии, что файл не удалялся и не перемещался 
в другую дирректорию. <br>
Есть так же встроенные в DC команды которыми вы можете пользоваться 
на любом хабе независимо от установленных скриптов на хабе<br>
<br>

<b>/join &lt;hub&gt;</b>...............присоединение к хабу <br>
<b>/ts</b>.............................проставляет время каждого 
сообщения, повторная команда отключает показ времени <br>
<b>/showjoins</b>................показывает пришедших и уходящих 
в основном окне, повторная команда отключает показ <br>
<b>/search (x)</b>...............поиск x <br>

<b>/clear</b>.......................очищает основное окно <br>
<b>/away (message)</b>....отправляет сообщение в авто-ответ <br>
<b>/back</b>.......................выключает команду /away <br>
<b>/slots (x)</b>..................изменяет количество слотов на 
x<br>
<b>/grant</b>.....................открытие grants a slot пользователю 
с которым вы приват чате <br>

<b>/close</b>......................закрытие активного окна <br>
<b>/refresh</b>..................обновление общих файлов <br>
<b>/help</b>.......................встроенные в DC команды <br>
<b>/dc++</b>.....................информация о DC и ссылка где можно 
скачать DC <br>
<b>/fav /favorite</b>.........добавляет активный хаб в &quot;избраное&quot;

</p>
