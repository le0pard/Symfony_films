<h2>General</h2>

<p>Теперь открываем <b>File &gt; Settings</b> ,появляется окно настроек</p>
<p>
<img border="0" src="/images/static_page/p2p/dc_v0.251.gif" width="439" height="461"></p>
<p>В поле <b>&quot;Nick&quot;</b> вводим свой ник-нейм (обязательный параметр)<br>

Далее в поле <b>&quot;E-Mail&quot;</b> вводим ваш e-mail адрес<br>
В поле <b>&quot;Description&quot;</b> желательно ввести тип информации, которую 
предполагаем отдать в пользование<br>
Выбираем свой <b>&quot;Connection&quot;</b> - тип связи с интернет-провайдером 
(не обязательный параметр)</p>

<h2><br>
<b>Connection Settings</b></h2>
<p><br>
<b>&quot;Active&quot;</b> если у вас нет FireWall оставляйте без изменений,в 
окне &quot;port&quot; можно указать номер порта с которым будет работать DC 
++<br>
<b>&quot;Passive&quot;</b> если вы работаете с FireWall обязательно отметте
<b>&quot;Passive&quot; </b>т.к. FireWall блокирует поиск. Пользователи в &quot;Passive 
Mode&quot; не могут соединяться с другими пользователями, кто находятся 
в &quot;Passive Mode&quot;.<br>

<b>&quot;SOCKS 5&quot;</b> это для тех кто работает через proxy</p>
<h2><b>Downloads</b></h2>
<p>
<img border="0" src="/images/static_page/p2p/dc_v0.306.gif" width="439" height="461"></p>
<p>Нажав на кнопку <b>&quot;Browse&quot; </b>в окне <b>Default downloads directory</b>, 
указываем, куда скачивать готовые файлы<br>

Нажав на кнопку <b>&quot;Browse&quot; </b>в окне <b>Unfinished downloads directory</b>, 
указываем, куда скачивать недокачаные файлы (после окончания закачки 
файлы с Unfinished downloads directory автоматически перемещаются 
в Default downloads directory)<br>
<br>
Раздел - <b>&quot;Options&quot;</b>. В нем вы можете указать количество ваших 
скачиваний в один момент времени если оставить &quot;0&quot;, то нет ограничений, 
вы, также, можете указать минимальную скорость скачивания, например, 
если скорость меньше &quot;10kB/s&quot; не скачивать ну и понятно, что значение 
ноль снимает данное ограничение</p>

<h2><b>Sharing</b></h2>
<p>
<img border="0" src="/images/static_page/p2p/dc_v0.305.gif" width="439" height="461"></p>
<p>Кнопкой <b>&quot;Add folder&quot;</b> можно добавить папку в шеринг, а 
кнопкой <b>&quot;Remove&quot;</b> мы удаляем папку из шеринга.<br>

Возможно, в папках находятся файлы с атрибутом <b>&quot;hidden&quot;</b>, 
если поставить галочку <b>&quot;Share hidden files&quot;</b> то они тоже будут 
отданы в шеринг.<br>
Опция <b>&quot;Atomatically open...&quot;</b> позволяет автоматически добавлять 
дополнительный слот<br>
Вы также, можете указать количество <b>&quot;Uploads slots&quot;</b>. Если 
3 &quot;слота&quot; открытые, тогда 3-e людей могут загружать данные.Cообщение
<b>&quot;No free slots&quot;</b> говорит о том, что Вы в очереди и начнете 
разгружать файл, как только появиться место.</p>

