<h2><b>Logs and Sounds</b></h2>
<p>
<img border="0" src="/images/static_page/p2p/dc_v0.263.gif" width="439" height="461"></p>

<p><b>&quot;Logging&quot; </b>- опция позволяет записывать логи главного чата,приват 
чата,downloads.uploads.<br>
<b>&quot;Sounds&quot;</b> - опция позволяет проиграть звуковой файл при получении 
